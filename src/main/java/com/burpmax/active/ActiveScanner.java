    /** Route an OOB hit to the correct probe's finding builder. */
    private static ActiveScanResult buildOobFinding(OobClient.OobHit hit) {
        String name = hit.probeName();
        if (name.equals("Log4Shell / Log4j RCE"))           return Log4ShellProbe.buildFinding(hit);
        if (name.equals("Blind XXE (OOB)"))                  return XxeProbe.buildOobFinding(hit);
        if (name.equals("SSRF (OOB)"))                       return buildGenericOobFinding(hit);
        if (name.equals("LDAP Injection (Blind OOB)"))       return LdapInjectionProbe.buildFinding(hit);
        if (name.equals("OS Command Injection (Blind OOB)")) return BlindCmdiProbe.buildFinding(hit);
        if (name.startsWith("SSTI → RCE (Blind OOB)"))      return BlindSstiProbe.buildFinding(hit);
        if (name.startsWith("SQL Injection (OOB DNS)"))      return OobSqliProbe.buildFinding(hit);
        if (name.startsWith("Prototype Pollution → RCE"))   return BlindPrototypePollutionProbe.buildFinding(hit);
        if (name.equals("JWT x5u SSRF"))                      return buildGenericOobFinding(hit);
        return buildGenericOobFinding(hit);
    }

    private static ActiveScanResult buildGenericOobFinding(OobClient.OobHit hit) {
        return new ActiveScanResult(
            hit.probeName(), com.burpmax.model.Finding.SEV_HIGH,
            "An out-of-band " + hit.interactionType() + " interaction was received from " +
            hit.endpoint() + " after injecting payload into '" + hit.parameter() + "'. " +
            "This confirms that the server initiated an outbound connection to the OOB host, " +
            "which is a strong indicator of a server-side request forgery or injection vulnerability.",
            "OOB: " + hit.interactionDetail() + " | Param: " + hit.parameter() +
            " | Payload: " + hit.payload(),
            "Investigate the injection point and determine the root cause. " +
            "Restrict outbound network access from the app tier.",
            "CWE-918",
            hit.endpoint(), hit.parameter(), hit.payload(), "", hit.interactionDetail(),
            hit.probeRequestBytes(), null, -1L);
    }

    /**
     * Runs the passive scanner over any response bytes carried by active probe results.
     * Catches version disclosures, secrets, security headers, and other passive findings
     * in error responses generated by active probes — the same behaviour as Burp Pro.
     *
     * Only processes results that carry non-null probeResponseBytes to avoid double-scanning
     * the same response (the original request/response is already handled by BurpExtender
     * via the IHttpListener path).
     */
    private void runPassiveOnProbeResults(List<ActiveScanResult> probeResults) {
        if (probeResults == null || probeResults.isEmpty()) return;
        for (ActiveScanResult r : probeResults) {
            if (r == null || r.probeResponseBytes == null) continue;
            if (r.probeRequestBytes == null) continue;
            try {
                // Parse probe request headers for context (method, URL, etc.)
                burp.IRequestInfo reqInfo  = helpers.analyzeRequest(r.probeRequestBytes);
                burp.IResponseInfo respInfo = helpers.analyzeResponse(r.probeResponseBytes);
                if (reqInfo == null || respInfo == null) continue;

                int statusCode = respInfo.getStatusCode();
                if (statusCode == 404) continue;  // same filter as BurpExtender.process()

                // Parse response headers
                java.util.Map<String, String> respHdrs  = new java.util.LinkedHashMap<>();
                java.util.List<String>        setCookies = new java.util.ArrayList<>();
                java.util.List<String>        hdrs = respInfo.getHeaders();
                int hdrStart = hdrs.size() > 1 ? 1 : 0;
                for (String h : hdrs.subList(hdrStart, hdrs.size())) {
                    int c = h.indexOf(':');
                    if (c < 0) continue;
                    String k = h.substring(0, c).trim().toLowerCase();
                    String v = h.substring(c + 1).trim();
                    respHdrs.put(k, v);
                    if ("set-cookie".equals(k)) setCookies.add(v);
                }

                // Response body (limit 256KB)
                int bodyOff = Math.min(respInfo.getBodyOffset(), r.probeResponseBytes.length);
                int bodyLen = Math.min(r.probeResponseBytes.length - bodyOff, 256_000);
                String body = helpers.bytesToString(
                        java.util.Arrays.copyOfRange(r.probeResponseBytes, bodyOff, bodyOff + bodyLen));

                // Request headers and body
                java.util.Map<String, String> reqHdrs = new java.util.LinkedHashMap<>();
                for (String h : reqInfo.getHeaders()) {
                    int c = h.indexOf(':');
                    if (c < 0) continue;
                    reqHdrs.put(h.substring(0, c).trim().toLowerCase(), h.substring(c + 1).trim());
                }
                int reqBodyOff = reqInfo.getBodyOffset();
                String reqBody = reqBodyOff < r.probeRequestBytes.length
                        ? helpers.bytesToString(java.util.Arrays.copyOfRange(
                              r.probeRequestBytes, reqBodyOff, r.probeRequestBytes.length))
                        : "";

                java.net.URL urlObj = reqInfo.getUrl();
                String url  = urlObj != null ? urlObj.toString() : r.endpoint;
                String host = urlObj != null ? urlObj.getHost()  : r.endpoint;
                String ct   = respHdrs.getOrDefault("content-type", "");

                // Dispatch through the passive scanner — reuses all 13 existing checkers
                java.util.List<com.burpmax.model.Finding> passiveFindings =
                    com.burpmax.scanner.Dispatcher.dispatch(
                        reqHdrs, respHdrs, setCookies,
                        body, ct,
                        reqInfo.getMethod(),
                        host, url, statusCode,
                        reqBody,
                        r.probeRequestBytes, r.probeResponseBytes,
                        null);  // null httpService is safe: UIBuilder.sendToRepeater() null-checks
                               // svcObj before casting, and FindingStore passes it through verbatim.
                               // These findings will show "cannot send to Repeater" but won't NPE.

                for (com.burpmax.model.Finding f : passiveFindings) {
                    // Tag the source so findings pane shows these came from active probe responses
                    f.analystNote = "Detected in active probe response (not original traffic)";
                    boolean isNew = store.add(f);
                    if (isNew) log("[PASSIVE-ON-PROBE] " + f.name + " | " + url);
                }
            } catch (Exception e) {
                // Non-fatal — passive scan on probe responses must never abort the active scan
                log("runPassiveOnProbeResults error (non-fatal): " + e.getMessage());
            }
        }
    }

    private Finding toFinding(ActiveScanResult r, ProbeContext ctx) {
        int sc = ctx.originalResponse != null ? extractStatusFromResponse(ctx.originalResponse) : 0;
        // Store the original traffic as the primary raw request/response
        // so passive checkers and the Repeater button work correctly.
        // The probe-specific data (mutated request, probe response, payload, timing)
        // is carried separately for PoC rendering.
        com.burpmax.model.Finding f = new com.burpmax.model.Finding(
                r.name, r.severity, r.description,
                r.evidence, r.remediation, r.cwe,
                ctx.host, ctx.url, sc,
                ctx.originalRequest, ctx.originalResponse, ctx.service);
        // Patch the primary EndpointEntry with probe-specific PoC data
        if (!f.affectedEndpoints.isEmpty()) {
            com.burpmax.model.Finding.EndpointEntry orig = f.affectedEndpoints.get(0);
            f.affectedEndpoints.set(0, new com.burpmax.model.Finding.EndpointEntry(
                    orig.endpoint, orig.statusCode,
                    orig.rawRequest, orig.rawResponse, orig.httpService,
                    r.probeRequestBytes, r.probeResponseBytes,
                    r.payload, r.parameter, r.timingMs));
        }
        return f;
    }

    private static int extractStatusFromResponse(byte[] resp) {
        if (resp == null || resp.length < 12) return 0;
        try {
            String start = new String(resp, 0, Math.min(20, resp.length));
            String[] parts = start.split(" ", 3);
            return parts.length > 1 ? Integer.parseInt(parts[1].trim()) : 0;
        } catch (Exception e) { return 0; }
    }

