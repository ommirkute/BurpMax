    /**
     * Build the set of URL+method keys already collected from the site map.
     * Passed to LinkExtractor so it doesn't re-fetch already-known URLs.
     */
    private Set<String> seenUrlsForExtraction() {
        Set<String> seen = new HashSet<>();
        try {
            IHttpRequestResponse[] items = callbacks.getSiteMap(null);
            if (items == null) return seen;
            for (IHttpRequestResponse item : items) {
                if (item.getRequest() == null) continue;
                try {
                    IRequestInfo ri = helpers.analyzeRequest(item);
                    seen.add(ri.getMethod() + ":" + ri.getUrl().toString());
                    seen.add(ri.getUrl().toString()); // also plain URL for LinkExtractor dedup
                } catch (Exception ignored) {}
            }
        } catch (Exception ignored) {}
        return seen;
    }

    /**
     * Convert a freshly fetched IHttpRequestResponse into a ProbeContext.
     * Mirrors the parsing logic in collectTargets — kept in sync manually.
     * Returns null if the item is out of scope, static, or unparseable.
     */
    private ProbeContext buildProbeContext(IHttpRequestResponse item) {
        try {
            byte[] req  = item.getRequest();
            byte[] resp = item.getResponse();
            if (req == null) return null;

            IRequestInfo reqInfo = helpers.analyzeRequest(item);
            URL url = reqInfo.getUrl();
            if (!callbacks.isInScope(url)) return null;

            String path = url.getPath() != null ? url.getPath().toLowerCase() : "";
            if (isStaticResource(path)) return null;
            if (isSkippableUrl(url.toString())) return null;

            IHttpService svc = item.getHttpService();
            if (svc == null) {
                final String _host  = url.getHost();
                final int    _port  = url.getPort() > 0 ? url.getPort()
                                    : "https".equalsIgnoreCase(url.getProtocol()) ? 443 : 80;
                final String _proto = url.getProtocol();
                svc = new IHttpService() {
                    public String getHost()     { return _host; }
                    public int    getPort()     { return _port; }
                    public String getProtocol() { return _proto; }
                };
            }

            boolean isHttps = "https".equalsIgnoreCase(svc.getProtocol());

            Map<String, String> queryParams = parseQueryParams(url.getQuery());
            Map<String, String> bodyParams  = new LinkedHashMap<>();
            Map<String, String> jsonParams  = new LinkedHashMap<>();
            Map<String, String> xmlParams   = new LinkedHashMap<>();
            String bodyRaw = "";
            String reqCT   = "";

            Map<String, String> reqHeaders = new LinkedHashMap<>();
            List<String> headers = reqInfo.getHeaders();
            for (int i = 1; i < headers.size(); i++) {
                String h = headers.get(i);
                int colon = h.indexOf(':');
                if (colon > 0) {
                    String k = h.substring(0, colon).trim().toLowerCase();
                    String v = h.substring(colon + 1).trim();
                    reqHeaders.put(k, v);
                    if ("content-type".equals(k)) reqCT = v;
                }
            }

            int bodyOffset = reqInfo.getBodyOffset();
            if (req.length > bodyOffset) {
                bodyRaw = helpers.bytesToString(Arrays.copyOfRange(req, bodyOffset, req.length));
                String ctLower = reqCT.toLowerCase();
                if (ctLower.contains("application/x-www-form-urlencoded")) {
                    bodyParams = parseQueryParams(bodyRaw);
                } else if (ctLower.contains("application/json")) {
                    flattenJson(bodyRaw, "", jsonParams);
                } else if (ctLower.contains("xml")) {
                    com.burpmax.active.XmlBodyParser.flatten(bodyRaw, xmlParams);
                }
            }

            for (IParameter p : reqInfo.getParameters()) {
                if (p.getType() == IParameter.PARAM_URL)
                    queryParams.putIfAbsent(p.getName(), p.getValue());
                else if (p.getType() == IParameter.PARAM_BODY)
                    bodyParams.putIfAbsent(p.getName(), p.getValue());
            }

            return new ProbeContext(svc, req, resp,
                    url.toString(), reqInfo.getMethod(),
                    url.getHost(), isHttps,
                    queryParams, bodyParams, jsonParams, xmlParams,
                    bodyRaw, reqCT, reqHeaders);
        } catch (Exception e) { return null; }
    }

    // ── Target collection ─────────────────────────────────────────────────────

    private List<ProbeContext> collectTargets() {
        List<ProbeContext> targets = new ArrayList<>();
        Set<String> seen = new HashSet<>();

        try {
            IHttpRequestResponse[] items = callbacks.getSiteMap(null);
            if (items == null) return targets;

            for (IHttpRequestResponse item : items) {
                try {
                    byte[] req  = item.getRequest();
                    byte[] resp = item.getResponse();
                    if (req == null) continue;

                    IRequestInfo reqInfo = helpers.analyzeRequest(item);
                    URL url = reqInfo.getUrl();

                    // Only in-scope
                    if (!callbacks.isInScope(url)) continue;

                    // Dedup by URL + method
                    String key = reqInfo.getMethod() + ":" + url.toString();
                    if (!seen.add(key)) continue;

                    // Skip static resources
                    String path = url.getPath() != null ? url.getPath().toLowerCase() : "";
                    if (isStaticResource(path)) continue;

                    // Skip Socket.IO / WebSocket / long-poll URLs — these are
                    // persistent connections that will hang the scanner thread.
                    if (isSkippableUrl(url.toString())) continue;

                    IHttpService svc = item.getHttpService();
                    boolean isHttps  = "https".equalsIgnoreCase(svc.getProtocol());

                    // Parse parameters
                    Map<String, String> queryParams = parseQueryParams(url.getQuery());
                    Map<String, String> bodyParams  = new LinkedHashMap<>();
                    Map<String, String> jsonParams  = new LinkedHashMap<>();
                    Map<String, String> xmlParams   = new LinkedHashMap<>();
                    String bodyRaw  = "";
                    String reqCT    = "";

                    // Parse headers
                    Map<String, String> reqHeaders = new LinkedHashMap<>();
                    List<String> headers = reqInfo.getHeaders();
                    for (int i = 1; i < headers.size(); i++) {
                        String h = headers.get(i);
                        int colon = h.indexOf(':');
                        if (colon > 0) {
                            String k = h.substring(0, colon).trim().toLowerCase();
                            String v = h.substring(colon + 1).trim();
                            reqHeaders.put(k, v);
                            if ("content-type".equals(k)) reqCT = v;
                        }
                    }

                    // Parse body parameters
                    int bodyOffset = reqInfo.getBodyOffset();
                    if (req.length > bodyOffset) {
                        bodyRaw = helpers.bytesToString(
                                Arrays.copyOfRange(req, bodyOffset, req.length));
                        String ctLower = reqCT.toLowerCase();
                        if (ctLower.contains("application/x-www-form-urlencoded")) {
                            bodyParams = parseQueryParams(bodyRaw);
                        } else if (ctLower.contains("application/json")) {
                            // Flatten JSON body into dot-notation paths so every
                            // leaf value becomes an independently injectable parameter.
                            flattenJson(bodyRaw, "", jsonParams);
                        } else if (ctLower.contains("xml")) {
                            // Flatten XML body leaf nodes into dot-notation paths.
                            // e.g. <user><name>alice</name></user>
                            //   → {"user.name": "alice"}
                            com.burpmax.active.XmlBodyParser.flatten(bodyRaw, xmlParams);
                        }
                    }

                    // Parse Burp parameter objects (query + form-encoded only;
                    // JSON params come from our own flattener above)
                    for (IParameter p : reqInfo.getParameters()) {
                        String pname = p.getName();
                        String pval  = p.getValue();
                        if (p.getType() == IParameter.PARAM_URL) {
                            queryParams.putIfAbsent(pname, pval);
                        } else if (p.getType() == IParameter.PARAM_BODY) {
                            bodyParams.putIfAbsent(pname, pval);
                        }
                    }

                    ProbeContext pctx = new ProbeContext(
                            svc, req, resp,
                            url.toString(), reqInfo.getMethod(),
                            url.getHost(), isHttps,
                            queryParams, bodyParams, jsonParams, xmlParams,
                            bodyRaw, reqCT, reqHeaders);
                    // Always add endpoints: even parameterless ones are tested
                    // by header-injection probes (Log4Shell, SSRF, HostHeader, CORS).
                    // Only skip truly parameterless GET endpoints where no
                    // header probes fire either — i.e. never skip entirely.
                    targets.add(pctx);

                } catch (Exception ignored) {}
            }
        } catch (Exception e) {
            log("Error collecting targets: " + e.getMessage());
        }
        return targets;
    }

    private static boolean isStaticResource(String path) {
        // Skip static file extensions
        if (path.matches(".*\\.(js|css|png|jpg|jpeg|gif|ico|svg|woff|woff2|ttf|eot|" +
                         "mp4|mp3|pdf|zip|tar|gz|map)$")) return true;
        // Skip Socket.IO / Engine.IO polling and WebSocket upgrade endpoints —
        // these are long-lived connections that will hang the scanner indefinitely.
        // Patterns: /socket.io/, EIO=, transport=polling, transport=websocket
        String lower = path.toLowerCase();
        if (lower.contains("/socket.io") || lower.contains("/engine.io")) return true;
        return false;
    }

    private static boolean isSkippableUrl(String fullUrl) {
        if (fullUrl == null) return false;
        String lower = fullUrl.toLowerCase();
        // Skip WebSocket upgrade URLs
        if (lower.startsWith("ws://") || lower.startsWith("wss://")) return true;
        // Skip long-poll / SSE / Socket.IO query params regardless of path
        if (lower.contains("transport=polling") || lower.contains("transport=websocket")) return true;
        if (lower.contains("eio=") && lower.contains("socket.io")) return true;
        // Skip Server-Sent Events endpoints
        if (lower.contains("/events") && lower.contains("text/event-stream")) return true;
        return false;
    }

    private static Map<String, String> parseQueryParams(String query) {
        Map<String, String> map = new LinkedHashMap<>();
        if (query == null || query.isEmpty()) return map;
        for (String pair : query.split("&")) {
            int eq = pair.indexOf('=');
            if (eq < 0) continue;
            try {
                String k = URLDecoder.decode(pair.substring(0, eq), "UTF-8");
                String v = URLDecoder.decode(pair.substring(eq + 1), "UTF-8");
                map.put(k, v);
            } catch (Exception ignored) {}
        }
        return map;
    }

    /**
     * Recursively flatten a JSON object/array body into a map of dot-notation
     * paths → string-serialised leaf values, using the same lightweight string
     * scanner as RequestBuilder (no JSONObject API — avoids stub limitations).
     *
     * Examples:
     *   {"username":"alice","pass":"s3cr3t"}
     *     → {"username":"alice", "pass":"s3cr3t"}
     *
     *   {"user":{"name":"alice","age":30},"active":true}
     *     → {"user.name":"alice", "user.age":"30", "active":"true"}
     *
     *   {"ids":[1,2],"tags":["a","b"]}
     *     → {"ids.0":"1", "ids.1":"2", "tags.0":"a", "tags.1":"b"}
     *
     * @param json   raw JSON body string
     * @param prefix current dot-path prefix ("" for root call)
     * @param out    map to populate with path → value entries
     */
    private static void flattenJson(String json, String prefix, Map<String, String> out) {
        if (json == null) return;
        json = json.trim();
        if (json.isEmpty()) return;
        if (json.length() > 65_536) return;   // guard against huge payloads

        try {
            JsonWalker.flattenAt(json, 0, prefix, out, 0);
        } catch (Exception ignored) {
            // Malformed JSON — silently skip
        }
    }

